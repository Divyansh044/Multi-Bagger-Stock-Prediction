def page_leaderboard():
    """The main view: ranks all stocks by their AI score"""
    st.title("🏆 Multibagger Leaderboard")
    st.markdown("These are the top stocks ranked by our ML model's probability of achieving 5× returns in 3 years.")

    df = get_predictions()
    if df is None:
        st.error("Predictions not found. Please run `python src/predictor.py` first.")
        return

    # Info summary
    top_n = len(df)
    high_count = len(df[df['category'].str.contains("High")])
    med_count  = len(df[df['category'].str.contains("Moderate")])
    
    col1, col2, col3 = st.columns(3)
    col1.metric("Total Stocks Scored", top_n)
    col2.metric("🔥 High Potential Picks", high_count)
    col3.metric("⚡ Watchlist Candidates", med_count)
    st.markdown("---")

    # Clean display dataframe
    display_cols = ['symbol', 'company_name', 'sector', 'current_price', 'score_pct', 'category']
    headers      = ['Symbol', 'Company', 'Sector', 'CMP (₹)', 'AI Score', 'Rating']
    
    display_df = df[display_cols].copy()
    display_df.columns = headers
    
    # Format price
    display_df['CMP (₹)'] = display_df['CMP (₹)'].apply(lambda x: f"₹{x:,.2f}" if pd.notnull(x) else "N/A")

    st.dataframe(
        display_df,
        use_container_width=True,
        height=800,
        hide_index=False
    )

    st.caption(f"Data as of: {df['as_of_date'].iloc[0] if not df.empty else 'Unknown'}")


def page_deep_dive():
    """Detailed view for a single stock"""
    st.title("🔍 Stock Deep Dive")
    st.markdown("Analyze the AI's reasoning, technical indicators, and fundamentals for a specific stock.")

    df = get_predictions()
    if df is None:
        st.error("Predictions not found.")
        return

    symbols = df['symbol'].tolist()
    
    # Custom search or selection
    custom_symbol = st.text_input(
        "Search for ANY stock symbol (e.g., AAPL, NVDA, RELIANCE.NS):",
        placeholder="Enter Yahoo Finance ticker..."
    )
    
    st.markdown("...or select from the pre-scored leaderboard:")
    selected_symbol = st.selectbox(
        "Select from Leaderboard:",
        options=symbols,
        index=0  # default to the #1 ranked stock
    )

    # Use custom search if provided, else use dropdown
    final_symbol = custom_symbol.strip().upper() if custom_symbol else selected_symbol

    if not final_symbol:
        return

    st.markdown("---")
    
    # Predict in real-time to get full feature breakdown
    with st.spinner(f"Analyzing {final_symbol}..."):
        result = predict_single_stock(final_symbol)
        chart_df = get_chart_data(final_symbol)

    if not result:
        st.error("Failed to analyze stock.")
        return

    # Header section
    if final_symbol in df['symbol'].values:
        company_name = df[df['symbol'] == final_symbol]['company_name'].iloc[0]
    else:
        # For custom searches, try to get the name from yfinance, or just use the symbol
        import yfinance as yf
        try:
            company_name = yf.Ticker(final_symbol).info.get('shortName', 'Custom Stock')
        except:
            company_name = "Custom Stock"
            
    st.markdown(f"## {company_name} (`{final_symbol}`)")
    
    # Main grid
    colA, colB = st.columns([1, 1.5])

    with colA:
        # The AI Score Gauge
        plot_gauge(result['score'], result['category'])
        st.markdown(f"<h3 style='text-align: center;'>{result['category']}</h3>", unsafe_allow_html=True)
        st.caption(f"Current Price: ₹{result['current_price']:,.2f}  |  As of: {result['as_of_date']}")

    with colB:
        # Price Chart
        plot_candlestick(chart_df, final_symbol)

    st.markdown("### 🔬 Why did the AI give this score?")
    st.markdown("Here are the underlying technical and fundamental signals driving the prediction:")

    # Feature metrics grid
    features = result['key_features']
    
    f_cols = st.columns(5)
    
    # Row 1 - Technicals
    f_cols[0].metric("RSI (14)", features.get("RSI (14)", "N/A"), help=">70 overbought, <30 oversold")
    f_cols[1].metric("MACD Hist", features.get("MACD Histogram", "N/A"), help="Positive = Bullish trend")
    f_cols[2].metric("Trend Ratio", features.get("MA50/MA200 Ratio", "N/A"), help=">1.0 means MA50 > MA200 (Golden Cross)")
    f_cols[3].metric("Momentum (3m)", f"{features.get('Momentum (3m)', 0)*100:.1f}%", help="Price return over 3 months")
    f_cols[4].metric("Vol Spike", f"{features.get('Volume Spike', 0)}x", help="Today's volume vs 30-day average")

    st.markdown("<br>", unsafe_allow_html=True)
    f_cols2 = st.columns(5)
    
    # Row 2 - Fundamentals
    f_cols2[0].metric("P/E Ratio", features.get("P/E Ratio", "N/A"), help="Price to Earnings")
    f_cols2[1].metric("P/B Ratio", features.get("P/B Ratio", "N/A"), help="Price to Book Value")
    f_cols2[2].metric("ROE", f"{features.get('ROE', 0)*100:.1f}%", help="Return on Equity (>15% is good)")
    f_cols2[3].metric("Rev Growth", f"{features.get('Revenue Growth', 0)*100:.1f}%", help="YoY Revenue Growth")
    f_cols2[4].metric("Debt/Equity", features.get("Debt/Equity", "N/A"), help="Financial risk (<1.0 is healthy)")


