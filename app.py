@st.cache_data(ttl=3600)  # Cache for 1 hour
def get_predictions():
    """Load the precomputed leaderboard from CSV"""
    df = load_predictions()
    if df is not None:
        # Convert score to percentage format for display
        df['score_pct'] = (df['multibagger_score'] * 100).map('{:.1f}%'.format)
    return df


@st.cache_data(ttl=3600)
def get_chart_data(symbol: str):
    """Download OHLCV data for charts (last 6 months is enough for plotting)"""
    return download_latest_price(symbol, lookback_days=180)


# =============================================================================
#  UI COMPONENTS: CHARTS & GAUGES
# =============================================================================

def plot_candlestick(df: pd.DataFrame, symbol: str):
    """Draw an interactive candlestick chart using Plotly"""
    if df is None or df.empty:
        st.warning(f"No chart data available for {symbol}")
        return

    fig = go.Figure(data=[go.Candlestick(
        x=df.index,
        open=df['Open'],
        high=df['High'],
        low=df['Low'],
        close=df['Close'],
        name='Price'
    )])

    # Add 20-day Simple Moving Average
    df['SMA20'] = df['Close'].rolling(window=20).mean()
    fig.add_trace(go.Scatter(
        x=df.index, y=df['SMA20'],
        line=dict(color='orange', width=1.5),
        name='20-Day MA'
    ))

    fig.update_layout(
        title=f"{symbol} - 6 Month Price History",
        yaxis_title="Price",
        xaxis_rangeslider_visible=False,
        height=400,
        margin=dict(l=0, r=0, t=40, b=0),
        template="plotly_dark"
    )
    st.plotly_chart(fig, use_container_width=True)


def plot_gauge(score: float, category: str):
    """Draw a speedometer-style gauge for the AI score"""
    color = "green" if "High" in category else "orange" if "Moderate" in category else "blue"

    fig = go.Figure(go.Indicator(
        mode="gauge+number",
        value=score * 100,
        domain={'x': [0, 1], 'y': [0, 1]},
        title={'text': "Multibagger Probability", 'font': {'size': 18}},
        gauge={
            'axis': {'range': [None, 100], 'tickwidth': 1, 'tickcolor': "white"},
            'bar': {'color': color},
            'bgcolor': "rgba(0,0,0,0)",
            'borderwidth': 2,
            'bordercolor': "gray",
            'steps': [
                {'range': [0, 45], 'color': "rgba(41, 98, 255, 0.2)"},
                {'range': [45, 70], 'color': "rgba(255, 214, 0, 0.2)"},
                {'range': [70, 100], 'color': "rgba(0, 200, 83, 0.2)"}
            ],
            'threshold': {
                'line': {'color': "white", 'width': 4},
                'thickness': 0.75,
                'value': score * 100
            }
        }
    ))

    fig.update_layout(
        height=300,
        margin=dict(l=10, r=10, t=40, b=10),
        paper_bgcolor="rgba(0,0,0,0)",
        font={'color': "white", 'family': "Arial"}
    )
    st.plotly_chart(fig, use_container_width=True)


